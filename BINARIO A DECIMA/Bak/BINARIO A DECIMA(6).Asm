.386
.MODEL FLAT,STDCALL
include masm32rt.inc 
.DATA

	BINA BYTE "111",0
	BASE BYTE 2

.CODE
	
	MAIN PROC
		
		MOV ESI,OFFSET BINA
		CALL LONGITUD ;RESULTADO EN ECX
		CALL ITERA
		inkey
		INVOKE ExitProcess,0
				
		RET

	MAIN ENDP
	
	LONGITUD PROC USES ESI
		
		MOV ECX,0
		CICLO1:
		
			CMP [ESI],BYTE PTR 0	;COMPARA SI SON IGUALES
			JZ SALIR				;SI SO IGUALES SALE SI NO CONTINUA	
			JNZ CONTINUA
		
		CONTINUA:
					
			INC ESI
			INC ECX
			JMP CICLO1
						
		SALIR:
		
			RET
			
	LONGITUD ENDP
	
	ITERA PROC USES ESI
	
		CICLO2:
			PUSHAD
			printf("e")			
			DEC ECX
			POPAD
		LOOP CICLO2
		
		RET 

	ITERA ENDP
	
	
	
	END MAIN